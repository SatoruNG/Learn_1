# пользовательский ввод
my_string = input()
# длина введенного текста
print(len(my_string))
# введенный текст в верхнем регистре
print(my_string.upper())
# введенный текст в нижнем регистре
print(my_string.lower())
# введенный текст без пробелов
my_string = my_string.replace(" ", "")
# первый символ введенного текста
print(my_string[0])
# последний символ введенного текста
print(my_string[-1])